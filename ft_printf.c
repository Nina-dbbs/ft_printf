#include "ft_printf.h"

int	ft_printf(const char *str, ...)
{
	va_list	args;
	int		len;
	int		i;

	len = 0;
	i = 0;
	if (!str)
		return (-1);
	va_start(args, str);
	while (str[i])
	{
		if (str[i] == '%')
		{
			len += ft_check_format(str[i + 1], args);
			i++;
		}
		else
			len += ft_putchar_count(str[i]);
		i++;
	}
	va_end(args);
	return (len);
}
